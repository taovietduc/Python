# Tìm số lớn nhất trong ba số

# Nhập ba số từ người dùng
a = float(input("Nhập số thứ nhất: "))
b = float(input("Nhập số thứ hai: "))
c = float(input("Nhập số thứ ba: "))

# Tìm số lớn nhất
so_lon_nhat = max(a, b, c)

# In kết quả
print("Số lớn nhất là:", so_lon_nhat)
