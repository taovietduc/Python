# Tính số lượng ký tự 'a' trong chuỗi: Nhập một chuỗi và đếm số lượng ký tự 'a'

# Nhập chuỗi từ bàn phím
chuoi = input("Nhập một chuỗi: ")

# Đếm số lượng ký tự 'a' trong chuỗi
so_luong_a = chuoi.count('a') # Hàm count() trả về số lần xuất hiện của một ký tự trong chuỗi

# In kết quả
print(f"Số lượng ký tự 'a' trong chuỗi là: {so_luong_a}")
